// === Sprach-Umschaltung / Rendering ===
function applyTranslations(lang) {
  const dict = translations[lang] || translations.de;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = (dict && dict[key]) || translations.de[key] || "";
    if (el.tagName === 'TITLE') {
      // Meta-Title korrekt setzen
      document.title = val || document.title;
    } else {
      el.textContent = val;
    }
  });

  // Buttons visuell markieren
  document.querySelectorAll('.language-switcher button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
    btn.setAttribute('aria-pressed', btn.dataset.lang === lang ? 'true' : 'false');
  });
}

function setLanguage(lang) {
  localStorage.setItem('lang', lang);
  applyTranslations(lang);
}

// Events verdrahten
document.addEventListener('DOMContentLoaded', () => {
  const stored = localStorage.getItem('lang') || 'de';
  applyTranslations(stored);
  document.querySelectorAll('.language-switcher button').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
  });
});
